    private void jLabel19MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel19MouseClicked
        try {
            if (jLabel7.getText().equals("Odaberite račun")) {
                JOptionPane.showMessageDialog(this, "Odaberite uslugu za koju želite da platite račun.");
                return;
            } else if (Double.valueOf(jTextField1.getText()) <= 0) {
                JOptionPane.showMessageDialog(this, "U polje za iznos uplate upišite vrednost veću od 0!");
                return;
            } else if (Integer.valueOf((String)jComboBox1.getSelectedItem()) > ZonedDateTime.now(ZoneId.of("Europe/Belgrade")).getMonthValue()
                    && Integer.valueOf((String)jComboBox2.getSelectedItem()) == ZonedDateTime.now(ZoneId.of("Europe/Belgrade")).getYear()) {
                JOptionPane.showMessageDialog(this, "Račun koji plaćate mora biti za tekući ili neki raniji mesec.");
                return;
            } else if (jLabel7.getText().equalsIgnoreCase("internet") && buttonGroup1.getSelection().getActionCommand() == null) {
                JOptionPane.showMessageDialog(this, "Odaberite internet provajdera!");
            } else {
                String name = jTextField3.getText().trim() + " " + jTextField2.getText().trim();
                String street = jTextField10.getText() + " " + jTextField12.getText();
                String month = "";
                if(String.valueOf(jComboBox3.getSelectedItem()).length()==1){ 
                    month = "0" + String.valueOf(jComboBox3.getSelectedItem());
                }else{
                    month = String.valueOf(jComboBox3.getSelectedItem());
                }
                String billMonth = (String)jComboBox1.getSelectedItem() + (String)jComboBox2.getSelectedItem();
                String validThru = month + String.valueOf(jComboBox4.getSelectedItem()).substring(2);
                String provider = "";
                if (jLabel7.getText().equalsIgnoreCase("internet")) {
                    provider = buttonGroup1.getSelection().getActionCommand();
                }
                String PAN = jTextField4.getText().trim() + jTextField5.getText().trim() + jTextField6.getText().trim() + jTextField7.getText().trim();
                BigDecimal.valueOf(Double.parseDouble(jTextField1.getText().trim()));
                String amount = jTextField1.getText().trim();
                String receiver = "";
                if (jLabel7.getText().equals("Struja")) {
                    receiver = "EPS";
                } else if (jLabel7.getText().equals("Infostan")) {
                    receiver = "INF";
                } else {
                    receiver = buttonGroup1.getSelection().getActionCommand();
                }
                String cvv2 = jTextField11.getText().trim();
                String ret = client.sendRequest(PAN, amount, receiver, cvv2, name, street, validThru, provider, billMonth);
                switch (ret){
                    case "00":
                        ret = "Račun je uspešno plaćen!";
                        break;
                    case "01":
                        ret = "Račun nije plaćen! Nedovoljno sredstava na tekućem računu!";
                        break;
                    case "02":
                        ret = "Račun nije plaćen! Pogrešan cvv kod!";
                        break;
                    case "03":
                        ret = "Račun nije plaćen! Navedeni klijent nije vlasnik kartice!";
                        break;
                    case "04":
                        ret = "Račun nije plaćen! Pogrešna adresa klijenta!";
                        break;
                    case "05":
                        ret = "Račun je već plaćen!";
                        break;
                    default:
                        ret = "Račun nije plaćen! Došlo je do greške!";
                }
                JOptionPane.showMessageDialog(this, ret);

            }
        } catch (Exception e) {
            //Timestamp ts = Timestamp.valueOf(LocalDateTime.now(ZoneId.of("UTC")));
            JOptionPane.showMessageDialog(this, "Račun nije plaćen! Došlo je do greške!");
            System.out.println(e);
        }
    }//GEN-LAST:event_jLabel19MouseClicked

