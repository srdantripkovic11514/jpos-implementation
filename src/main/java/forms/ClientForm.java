    private void comboColor(JComboBox box) {
        box.setRenderer(new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList list, Object value,
                    int index, boolean isSelected, boolean cellHasFocus) {
                Component c = super.getListCellRendererComponent(
                        list, value, index, isSelected, cellHasFocus);
                list.setSelectionForeground(Color.WHITE);
                list.setSelectionBackground(new Color(0, 102, 153));
                return c;
            }

        });
    }

    private void populateComboYears(JComboBox jcb, int j, boolean b) {
        jcb.removeAllItems();
        if (!b) {
            for (int i = 0; i < j; i++) {
                jcb.addItem(String.valueOf(ZonedDateTime.now(ZoneId.of("Europe/Belgrade")).minusYears(i).getYear()));
            }
        } else {
            for (int i = 0; i < j; i++) {
                jcb.addItem(String.valueOf(ZonedDateTime.now(ZoneId.of("Europe/Belgrade")).plusYears(i).getYear()));
            }
        }
    }

